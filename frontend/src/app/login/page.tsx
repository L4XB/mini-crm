'use client';

import React, { useState } from 'react';
import { useForm } from 'react-hook-form';
import { authService } from '@/services/api';

// Authentifizierungskontext
const useAuth = () => {
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const login = async (email: string, password: string) => {
    setIsLoading(true);
    setError(null);
    
    console.log('Versuche Login mit:', { email, password: '***' });
    
    try {
      // Verwende den echten Authentifizierungsdienst
      console.log('Versuche Login mit echtem API-Service...');
      const data = await authService.login(email, password);
      console.log('Login-Antwort:', data);
      
      localStorage.setItem('token', data.token);
      // refreshToken ist im Mock nicht implementiert
      localStorage.setItem('user', JSON.stringify(data.user));
      window.location.href = '/dashboard';
    } catch (err) {
      console.error('Login error:', err);
      setError(err instanceof Error ? err.message : 'Login fehlgeschlagen. Bitte überprüfen Sie Ihre Anmeldedaten.');
    } finally {
      setIsLoading(false);
    }
  };

  return {
    login,
    isLoading,
    error
  };
};

// Simplified Button component to avoid import issues
